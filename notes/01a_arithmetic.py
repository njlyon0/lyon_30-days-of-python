# Start with simple arithmetic
print(4 + 3)
print(1 - 2)
print(2 * 8)
print(7 / 6)
## Exponent operator is '**'
print(3 ** 2)
## Remainder found by '%'
print(1 % 2)
## Remainder removed by '//'
print(1 // 2)

